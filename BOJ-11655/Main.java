	static String ROT13(char[] arr) throws IOException{
		for (int i = 0; i < arr.length; i++) {
			int here = arr[i];
			if('a' <= here && here <= 'z') {
				here = here - 'a';
				here = (here + 13) % 26;
				arr[i] = (char)(here+'a');
			}
			else if('A' <= here && here <= 'Z') {
				here = here - 'A';
				here = (here + 13) % 26;
				arr[i] = (char)(here+'A');
			}
			else continue;
		}
		return new String(arr);
	}

	
	

